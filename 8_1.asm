ORG 0000h
_RESET:
    LJMP _INIT
ORG 0100h
_INIT:
    MOV SCON, #01010000b
    ANL TMOD, #00101111b
    ORL TMOD, #00100000b
    MOV TL1, #0FDh
    MOV TH1, #0FDh
    ANL PCON, #01111111b
    CLR TF1
    SETB TR1
_LOOP:
    JNB TI, _LOOP10
    CLR TI
_LOOP10:
    JNB RI, _LOOP
    CLR RI
    MOV SBUF, #'H'
    CLR P1.7
    LJMP _LOOP
END
